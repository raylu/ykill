DROP TABLE IF EXISTS "kill_characters";
DROP TABLE IF EXISTS "items";
DROP TABLE IF EXISTS "item_costs";
DROP TABLE IF EXISTS "kill_costs";
DROP TABLE IF EXISTS "kills";
DROP TABLE IF EXISTS "alliances";
DROP TABLE IF EXISTS "corporations";
DROP TABLE IF EXISTS "characters";
DROP TABLE IF EXISTS "wh_systems";

CREATE TABLE "kills" (
	"kill_id" serial PRIMARY KEY,
	"solar_system_id" integer NOT NULL,
	"kill_time" timestamp NOT NULL,
	"moon_id" integer NULL
);
CREATE INDEX "solar_system_id_kill_time" ON "kills" ("solar_system_id", "kill_time");
CREATE INDEX "kill_time" ON "kills" ("kill_time");

CREATE TABLE "kill_characters" (
	"id" serial PRIMARY KEY,
	"kill_id" integer NOT NULL REFERENCES "kills" ("kill_id"),
	"victim" boolean NOT NULL,
	"character_id" integer NOT NULL,
	"character_name" varchar(64) NOT NULL,
	"ship_type_id" integer NOT NULL,
	"alliance_id" integer NOT NULL,
	"alliance_name" varchar(64) NOT NULL,
	"corporation_id" integer NOT NULL,
	"corporation_name" varchar(64) NOT NULL,
	"faction_id" integer NOT NULL,
	"faction_name" varchar(64) NOT NULL,
	"damage" integer DEFAULT NULL,
	"final_blow" boolean DEFAULT NULL,
	"security_status" float DEFAULT NULL,
	"weapon_type_id" integer DEFAULT NULL
);
CREATE INDEX "character_id_kill_id" ON "kill_characters" ("character_id", "kill_id");
CREATE INDEX "alliance_id_kill_id" ON "kill_characters" ("alliance_id", "kill_id");
CREATE INDEX "corporation_id_kill_id" ON "kill_characters" ("corporation_id", "kill_id");
CREATE INDEX "ship_type_id_victim_kill_id" ON "kill_characters" ("ship_type_id", "victim", "kill_id");

CREATE TABLE "items" (
	"id" serial PRIMARY KEY,
	"kill_id" integer NOT NULL REFERENCES "kills" ("kill_id"),
	"type_id" integer NOT NULL,
	"flag" smallint NOT NULL,
	"dropped" integer NOT NULL,
	"destroyed" integer NOT NULL,
	"singleton" smallint NOT NULL
);

CREATE TABLE "item_costs" (
	"type_id" serial PRIMARY KEY,
	"cost" bigint NOT NULL
);

CREATE TABLE "kill_costs" (
	"kill_id" integer PRIMARY KEY REFERENCES "kills" ("kill_id"),
	"cost" bigint NOT NULL
);
CREATE INDEX "cost_kill_id" ON "kill_costs" ("cost", "kill_id");

CREATE TABLE "alliances" (
	"alliance_id" serial PRIMARY KEY,
	"alliance_name" varchar(64) NOT NULL,
	"killed" bigint NOT NULL,
	"lost" bigint NOT NULL
);
CREATE INDEX "alliance_name" ON "alliances" (LOWER("alliance_name"));

CREATE TABLE "corporations" (
	"corporation_id" serial PRIMARY KEY,
	"corporation_name" varchar(64) NOT NULL,
	"killed" bigint NOT NULL,
	"lost" bigint NOT NULL
);
CREATE INDEX "corporation_name" ON "corporations" (LOWER("corporation_name"));

CREATE TABLE "characters" (
	"character_id" serial PRIMARY KEY,
	"character_name" varchar(64) NOT NULL,
	"killed" bigint NOT NULL,
	"lost" bigint NOT NULL
);
CREATE INDEX "character_name" ON "characters" (LOWER("character_name"));

CREATE TABLE "wh_systems" (
	"id" serial PRIMARY KEY,
	"class" smallint NOT NULL,
	"effect" varchar(20) NULL,
	"static1" varchar(5) NULL,
	"static2" varchar(5) NULL
);

